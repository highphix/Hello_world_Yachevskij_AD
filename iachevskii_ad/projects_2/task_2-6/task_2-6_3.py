donor = input("Введите группу крови донора (0, a, b, ab): ").strip().upper()
recipient = input("Введите группу крови реципиента (0, a, b, ab): ").strip().upper()

if donor == recipient:
    print("Переливание возможно: группы крови совпадают.")
elif donor == "0" or recipient == "0":
    print("Переливание возможно: донор с нулевой группой (универсальный донор).")
else:
    print("Переливание невозможно: группы несовместимы.")